            _start.pt = result.location;
            
            BMKGeoCodeSearchOption *geocodeSearchOption = [[BMKGeoCodeSearchOption alloc]init];
            
            
            // 目的地转码
            NSMutableArray *array =(NSMutableArray*)[_string2 componentsSeparatedByString:@"-"];
            _endAddrText = @"";
            
            for (NSString *str in array) {
                _endAddrText = [NSString stringWithFormat:@"%@%@", _endAddrText ,str];
            }
            
            
            geocodeSearchOption.address = _endAddrText;
            
            BOOL flag = [_geocodesearch2 geoCode:geocodeSearchOption];
            
            
            if(flag)
            {
                NSLog(@"geo检索发送成功");
            }
            else
            {
